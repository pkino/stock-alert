/**
 * 半角カタカナ → 全角カタカナ変換関数
 * @param {string} text - 変換対象のテキスト
 * @returns {string} 変換後のテキスト
 */
function f7conv(text) {
  if (!text) return '';

  // 二文字の濁点・半濁点を先に変換
  const result = text.replace(/(ｳﾞ|[ｶ-ﾎ][ﾞﾟ])/g, m => hankataToZenkataMap[m] || m)
                     // 次に単体文字を変換
                     .replace(/[｡-ﾟ]/g, m => hankataToZenkataMap[m] || m);

  return result;
}

/**
 * HTMLエスケープ
 * @param {string} str - エスケープ対象の文字列
 * @returns {string} エスケープ後の文字列
 */
function escapeHtml(str) {
  return String(str)
    .replaceAll('&','&amp;')
    .replaceAll('<','&lt;')
    .replaceAll('>','&gt;')
    .replaceAll('"','&quot;')
    .replaceAll("'",'&#39;');
}
